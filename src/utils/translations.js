// Helper function to get the translated text
export const getText = (key, language = "en") => {
  if (!translations[language] || !translations[language][key]) {
    // Fallback to English if translation not found
    return translations["en"][key] || key;
  }
  return translations[language][key];
};

// Helper function to get the state name in the selected language
export const getStateName = (stateKey, language = "en") => {
  if (stateKey && stateData[stateKey] && stateData[stateKey][language]) {
    return stateData[stateKey][language];
  }
  return stateKey; // Fallback to key if translation not found
};

// Helper function to get the district name in the selected language
export const getDistrictName = (stateKey, districtKey, language = "en") => {
  if (
    stateKey &&
    districtKey &&
    stateData[stateKey] &&
    stateData[stateKey].districts &&
    stateData[stateKey].districts[districtKey] &&
    stateData[stateKey].districts[districtKey][language]
  ) {
    return stateData[stateKey].districts[districtKey][language];
  }
  return districtKey; // Fallback to key if translation not found
};

